import { _ } from "./i18n.js"
import { escapeHtml, showToast } from "./utils.js"

const CATEGORY_LABELS = {
  main: "Main",
  weekly: "Weekly",
  custom: "Custom",
  unknown: "Unknown",
}

const state = { candidates: [], selected: new Set(), discovered: false, scanning: false }

async function fetchJson(url, opts = {}) {
  const r = await fetch(url, opts)
  if (!r.ok) {
    const data = await r.json().catch(() => ({}))
    throw new Error(data.error || `HTTP ${r.status}`)
  }
  return r.json()
}

function confirmAction(message) {
  // biome-ignore lint/suspicious/noAlert: user confirmation for destructive action
  return window.confirm(message)
}

function trackedCardHtml(p) {
  const cat = escapeHtml(_(CATEGORY_LABELS[p.category] || "Unknown"))
  return `<div class="management-card pl-card">
    <div class="management-info pl-card-info">
      <span class="pl-cat pl-cat-${p.category}">${cat}</span>
      <span class="pl-card-title">${escapeHtml(p.title)}</span>
    </div>
    <div class="management-actions">
      <a class="playlist-link" href="https://music.youtube.com/playlist?list=${escapeHtml(p.id)}" target="_blank" rel="noopener">${_("Open")}</a>
      <button class="btn btn-danger btn-sm" data-action="deleteYtmPlaylist" data-id="${escapeHtml(p.id)}" data-name="${escapeHtml(p.title)}">${_("Delete")}</button>
    </div>
  </div>`
}

function untrackedRowHtml(p) {
  const cat = escapeHtml(_(CATEGORY_LABELS[p.category] || "Unknown"))
  const checked = state.selected.has(p.id) ? " checked" : ""
  return `<label class="pl-untracked-row">
    <input type="checkbox" data-pl-id="${escapeHtml(p.id)}"${checked}>
    <span class="pl-cat pl-cat-${p.category}">${cat}</span>
    <span class="pl-card-title">${escapeHtml(p.title)}</span>
  </label>`
}

function render() {
  const list = document.getElementById("playlistsList")
  if (!list) return
  if (!state.discovered) return
  const tracked = state.candidates.filter(p => p.tracked)
  const untracked = state.candidates.filter(p => !p.tracked)
  const badge = document.getElementById("playlistsTabBadge")
  if (badge) badge.textContent = tracked.length || ""

  if (tracked.length === 0 && untracked.length === 0) {
    list.innerHTML = `<div class="empty-state"><h3>${_("No playlists found")}</h3><p>${_(
      "No autogenerated playlists were detected in your library.",
    )}</p></div>`
    return
  }

  let html = tracked.map(trackedCardHtml).join("")
  if (untracked.length > 0) {
    const sel = state.selected.size
    html += `<div class="pl-untracked">
      <div class="pl-untracked-head">
        <span>${_("Discovered, not tracked")} (${untracked.length})</span>
        <button class="btn btn-secondary btn-sm" data-action="trackSelectedPlaylists"${sel === 0 ? " disabled" : ""}>${_("Track selected")}</button>
      </div>
      ${untracked.map(untrackedRowHtml).join("")}
    </div>`
  }
  list.innerHTML = html
  for (const cb of list.querySelectorAll("input[type=checkbox]")) {
    cb.addEventListener("change", () => {
      const id = cb.dataset.plId
      if (cb.checked) state.selected.add(id)
      else state.selected.delete(id)
      render()
    })
  }
}

export async function discoverPlaylists() {
  if (state.scanning) return
  state.scanning = true
  showToast(_("Scanning library..."), "info")
  try {
    const data = await fetchJson("/api/playlists/discover", { method: "POST" })
    state.candidates = data.candidates || []
    state.selected = new Set(state.candidates.filter(p => !p.tracked).map(p => p.id))
    state.discovered = true
    render()
    showToast(_("Found %(n)s playlists", { n: state.candidates.length }))
  } catch (e) {
    showToast(e.message, "error")
  } finally {
    state.scanning = false
  }
}

export async function loadTrackedPlaylists() {
  if (state.discovered) {
    render()
    return
  }
  try {
    const data = await fetchJson("/api/playlists/tracked")
    state.candidates = data.candidates || []
    state.discovered = true
    render()
  } catch {
    // leave default empty-state in place
  }
}

export async function trackSelectedPlaylists() {
  const items = state.candidates.filter(p => state.selected.has(p.id)).map(p => ({ title: p.title, id: p.id }))
  if (items.length === 0) {
    showToast(_("Nothing selected"), "error")
    return
  }
  try {
    const data = await fetchJson("/api/playlists/track", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ playlists: items }),
    })
    for (const p of state.candidates) {
      if (state.selected.has(p.id)) p.tracked = true
    }
    state.selected.clear()
    render()
    showToast(_("Tracked %(n)s playlists", { n: data.tracked }))
  } catch (e) {
    showToast(e.message, "error")
  }
}

export async function deleteYtmPlaylist(id, name) {
  if (!confirmAction(_("Delete '%(name)s' from YouTube Music? This cannot be undone.", { name }))) return
  try {
    await fetchJson("/api/playlists/delete", {
      method: "DELETE",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ id, name }),
    })
    state.candidates = state.candidates.filter(p => p.id !== id)
    state.selected.delete(id)
    render()
    showToast(_("Deleted"))
  } catch (e) {
    showToast(e.message, "error")
  }
}

export async function pruneWeeklies() {
  if (!confirmAction(_("Delete old weekly playlists from YouTube Music?"))) return
  try {
    const data = await fetchJson("/api/playlists/prune-weeklies", { method: "POST" })
    showToast(_("Pruned %(n)s playlists", { n: data.deleted.length }))
    discoverPlaylists()
  } catch (e) {
    showToast(e.message, "error")
  }
}
